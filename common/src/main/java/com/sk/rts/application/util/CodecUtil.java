    public static String encode64(long num) {
        StringBuilder sb = new StringBuilder();
        do {
            sb.append((char) CHAR_VALUES[(int) (num & 0x3F)]);
            num >>>= 6;
        } while (num > 0);
        return sb.reverse().toString();
    }

    public static long decode64(String n62) {
        byte[] bytes = n62.getBytes(StandardCharsets.US_ASCII);
        long num = 0;
        for (byte b : bytes) {
            num = num << 6 | (long) CHAT_INDEXES[b];
        }
        return num;
    }
